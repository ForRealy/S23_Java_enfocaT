package com.main;
public class Arit {
    public static void main(String args[]) {
    	int resultadoResta = 105 - 55;
    	System.out.println("Resultado de la resta: " + resultadoResta);
    	int resultadoSuma = 55 + 45;
    	System.out.println("Resultado de la suma: " + resultadoSuma);
    	int numeroNegativo = -3;
    	System.out.println("Numero negativo: " + numeroNegativo);
    	int resultadoMultiplicacion = 5 * 5;
    	System.out.println("Resultado de la multiplicacion: " + resultadoMultiplicacion);
    	int resultadoDivisionEntera = 25 / 7;
    	System.out.println("Resultado de la division entera: " + resultadoDivisionEntera);
    	double resultadoDivisionReal = 25.0 / 7.0;
    	System.out.println("Resultado de la division real: " + resultadoDivisionReal);
    	int restoDivision = 25 % 4;
    	System.out.println("Resto de la division: " + restoDivision);
    }
}
