package com.main;
public class Aritcomb {
    public static void main(String args[]) {
        int numero = 25;
        
        // Utiliza el operador de suma combinada para sumar 5 al valor actual
        numero += 5;
        System.out.println("Valor actual despues de sumar 5: " + numero);
        
        // Utiliza el operador de resta combinada para restar 15 al valor actual
        numero -= 15;
        System.out.println("Valor actual despues de restar 15: " + numero);
        
        // Utiliza el operador de producto combinado para multiplicar por 2 el valor actual
        numero *= 2;
        System.out.println("Valor actual despues de multiplicar por 2: " + numero);
        
        // Utiliza el operador de divisi√≥n combinada para dividir entre 2 el valor actual
        numero /= 2;
        System.out.println("Valor actual despues de dividir entre 2: " + numero);
        
        // Utiliza el operador de resto combinado para obtener el resto del valor actual dividido entre 3
        numero %= 3;
        System.out.println("Resto del valor actual dividido entre 3: " + numero);
    }
}
